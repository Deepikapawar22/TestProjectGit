package javaPackage;

public class Switch {

	public static void main(String[] args) {
		int age=18;
		switch(age) {
		case(16):
		System.out.println("you are under 18");
		break;
		case(18):
			System.out.println("you are eligible for vote");
		break;
		case(65):
			System.out.println("you are senior");
		}

	}

}
