package javaPackage;

public class IfNestedIf {

	public static void main(String[] args) {
		int q=200;
		int w=200;
		
		if (q<w) {
			System.out.println("q is lesser");
			
		}else if (q==w) {
			System.out.println("q is equal to w");

		}else {
			System.out.println("q is greater");
		}

	}

}
